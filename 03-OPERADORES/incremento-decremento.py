# Preferiblemente usar operadores de asignación o operadores aritméticos!!!

year = 2021

# Operador de incremento
year = year + 1
print(year)

# Operador de preincremento
year = 1 + year
print(year)

# Operador de decremento
year = year - 1
print(year)

# Operador de predecremento
year = 1 - year
print(year)
