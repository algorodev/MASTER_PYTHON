# Operador de asignación =
edad = 23
print(edad)

# Operador que suma y asigna al valor anterior +=
edad += 7
print(edad)

# Operador que suma y asigna al valor anterior -=
edad -= 7
print(edad)

# Operador que multiplica y asigna al valor anterior *=
edad *= 2
print(edad)

# Operador que divide y asigna al valor anterior /=
edad /= 2
print(edad)

# Operador que devuelve el resto de la división y asgina al valor anterior %=
edad %= 2
print(edad)
