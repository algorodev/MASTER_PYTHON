numero1 = 19
numero2 = 97

# Operación de suma +
suma = numero1 + numero2
print(f"El valor de la suma es: {suma}")

# Operación de resta -
resta = numero1 - numero2
print(f"El valor de la suma es: {resta}")

# Opración de multiplicar *
multiplicacion = numero1 * numero2
print(f"El valor de la multiplicación es: {multiplicacion}")

# Operación de división /
division = numero1 / numero2
print(f"El valor de la división es: {division}")

# Opración que nos devuelve el resto de una división %
resto = numero1 % numero2
print(f"El valor del resto de la división es: {resto}")
